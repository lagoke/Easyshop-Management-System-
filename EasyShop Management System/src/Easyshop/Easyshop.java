    private void EqualBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EqualBtnActionPerformed
        String answer;
        secondnum = Double.parseDouble(InputTxf.getText());
        if (null != operations)
        switch (operations) {
           case "+" -> {
               result = firstnum + secondnum;
               answer = String.format("%.0f", result);
               InputTxf.setText(answer);
            }
           case "-" -> {
               result = firstnum - secondnum;
               answer = String.format("%.0f", result);
               InputTxf.setText(answer);
            }
           case "*" -> {
               result = firstnum * secondnum;
               answer = String.format("%.0f", result);
               InputTxf.setText(answer);
            }
           case "/" -> {
               result = firstnum / secondnum;
               answer = String.format("%.0f", result);
               InputTxf.setText(answer);
            }
           case "%" -> {
               result = firstnum % secondnum;
               answer = String.format("%.0f", result);
               InputTxf.setText(answer);
            }
           default -> {
               break;
            }
       }
                             
    }//GEN-LAST:event_EqualBtnActionPerformed

